for (const locale of Object.keys(resources) as Array<keyof typeof resources>) {
  const translation = resources[locale].translation as typeof resources[typeof locale]["translation"] & {
    receive?: unknown
    transfer?: unknown
  }

  if (!translation.receive && translation.home && "receive" in translation.home) {
    translation.receive = translation.home.receive
  }

  if (!translation.transfer && translation.home && "transfer" in translation.home) {
    translation.transfer = translation.home.transfer
  }
}

void i18n.use(initReactI18next).init({
  compatibilityJSON: "v4",
  lng: DEFAULT_LANGUAGE,
  fallbackLng: DEFAULT_LANGUAGE,
  resources,
  interpolation: {
    escapeValue: false,
  },
})

export { i18n }

export async function hydrateI18n() {
  const storedLanguage = getString(KvStorageKeys.AppLanguage)

  if (storedLanguage && storedLanguage !== i18n.language) {
    await i18n.changeLanguage(storedLanguage)
  }
}

export async function setLanguage(language: string) {
  setString(KvStorageKeys.AppLanguage, language)
  await i18n.changeLanguage(language)
}

export function getCurrentLanguage() {
  return i18n.language || DEFAULT_LANGUAGE
}
